    private void currentDate() {
        Calendar cal = new GregorianCalendar();
        int month = cal.get(Calendar.MONTH);
        int year = cal.get(Calendar.YEAR);
        int day = cal.get(Calendar.DAY_OF_MONTH);
        DateField.setText(day + "/" + (month + 1) + "/" + year);

    }

    private void removeAllRow() {
        DefaultTableModel m = (DefaultTableModel) ProductHouseTable.getModel();
        int rowCount = m.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            m.removeRow(i);
        }
    }

    private void showTableData() {
        try {
            Connection conn = DatabaseConnector.connect();
            DefaultTableModel model = (DefaultTableModel) ProductHouseTable.getModel();
            String sql = "select * from producthouse"
                    + " where Code=? ";

            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(ProductCodeField.getText()));
            ResultSet rs = pst.executeQuery();
            //removeAllRow();
            while (rs.next()) {
                int code = rs.getInt("Code");
                String name = rs.getString("Name");
                int qty = rs.getInt("Qty");
                double price = rs.getInt("Price");
                double sellPrice = rs.getDouble("SellPrice");
                String date = rs.getString("Date");

                Object[] row = {code, name, qty, price, sellPrice, date};

                model.addRow(row);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }

