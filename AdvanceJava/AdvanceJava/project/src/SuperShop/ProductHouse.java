    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        String code = ProductCodeField.getText();
        String name = ProductNameField.getText();
        String qty = ProductQtyField.getText();
        String price = ProductPriceField.getText();
        String sellprice = SellPriceField.getText();
        String date = DateField.getText();

        try {
            Connection conn = DatabaseConnector.connect();

            String sql = "INSERT INTO productHouse(Code,Name,Qty,Price,sellprice,Date)"
                    + "values(?,?,?,?,?,?)";

            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, code);
            pst.setString(2, name);
            pst.setString(3, qty);
            pst.setString(4, price);
            pst.setString(5, sellprice);
            pst.setString(6, date);
            int rs = pst.executeUpdate();

            if (rs > 0) {
                JOptionPane.showMessageDialog(rootPane, "Saved successfully");

                formReset();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Saved faild");
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ProductHouse.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ProductHouse.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_BtnSaveActionPerformed

