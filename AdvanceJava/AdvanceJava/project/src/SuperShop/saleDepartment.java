    private void removeAllRow() {
        DefaultTableModel m = (DefaultTableModel) SaleTable.getModel();
        int rowCount = m.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            m.removeRow(i);
        }
    }

