    private void UpdateQty() {

        try {

            Connection conn = DatabaseConnector.connect();
            String sql = " update producthouse "
                    + " set Qty=? "
                    + " where Code=? ";
            PreparedStatement pst = conn.prepareStatement(sql);
            int aqty = Integer.parseInt(AQtyField.getText());
            int sqty = Integer.parseInt(SaleQty.getText());
            int currentQty = aqty - sqty;
            pst.setInt(1, currentQty);

            pst.setInt(2, Integer.parseInt(ProductCodeField.getText()));
            int rs = pst.executeUpdate();

            if (rs > 0) {
                System.out.println("Qty update");
            } else {
                System.out.println("Qty updated fail!");
            }

        } catch (ClassNotFoundException ex) {
            System.out.println(ex.getMessage());
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }

    }

