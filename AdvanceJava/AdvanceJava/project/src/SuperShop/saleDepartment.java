    private void searchIconMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchIconMouseClicked
        try {
            Connection conn = DatabaseConnector.connect();
            String sql = " select * from producthouse "
                    + " where Code=? ";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(ProductCodeField.getText()));
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                AQtyField.setText(rs.getString("Qty"));
                NameField.setText(rs.getString("Name"));
                PriceField.setText(rs.getString("SellPrice"));
            } else {
                JOptionPane.showMessageDialog(rootPane, "no work !");
            }

        } catch (ClassNotFoundException | SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_searchIconMouseClicked

