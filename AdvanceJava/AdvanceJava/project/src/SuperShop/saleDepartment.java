    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        String customarName = CustomarNameField.getText();
        String code = ProductCodeField.getText();
        String name = NameField.getText();
        String price = PriceField.getText();
        String aQty = AQtyField.getText();
        String sQty = SaleQty.getText();
        String date = DateField.getText();
        try {
            Connection conn = DatabaseConnector.connect();
            String sql = " INSERT INTO saledepartment(CustomarName,Code,Name,Price,AQty,SQty,Date) "
                    + " values(?,?,?,?,?,?,?) ";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, customarName);
            pst.setString(2, code);
            pst.setString(3, name);
            pst.setString(4, price);
            pst.setString(5, aQty);
            pst.setString(6, sQty);
            pst.setString(7, date);
            int rs = pst.executeUpdate();

            if (rs > 0) {
                JOptionPane.showMessageDialog(rootPane, "saved successfully");
                showDataInTable();
                UpdateQty();
                resetField();

            } else {
                JOptionPane.showMessageDialog(rootPane, "saved fail!");
            }

        } catch (ClassNotFoundException ex) {
            System.out.println(ex.getMessage());
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_AddBtnActionPerformed

