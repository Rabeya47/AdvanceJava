      private void currentDate() {
        Calendar cal = new GregorianCalendar();
        int month = cal.get(Calendar.MONTH);
        int year = cal.get(Calendar.YEAR);
        int day = cal.get(Calendar.DAY_OF_MONTH);
        DateField.setText(day + "/" + (month + 1) + "/" + year);

    }

    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        String id = EmployeeIdField.getText();
        String name = EmployeeNameField.getText();
        String position = EmployeePositionField.getText();
        String salary = EmployeeSalaryField.getText();
        String contact = ContactField.getText();
        String address =  AddressField.getText();
        String date = DateField.getText();

        try {
            Connection conn = DatabaseConnector.connect();
            String sql = "INSERT INTO employeetable(Id,Name,Position,Salary,Contact,Address,Date)"
                    + "values(?,?,?,?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, id);
            pst.setString(2, name);
            pst.setString(3, position);
            pst.setString(4, salary);
            pst.setString(5, contact);
            pst.setString(6, address);
            pst.setString(7,date);
            int rs = pst.executeUpdate();

            if (rs > 0) {
                JOptionPane.showMessageDialog(rootPane, "Saved successfully");
                //showTableData();
                formReset();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Saved unsuccessfully");
            }

        } catch (ClassNotFoundException | SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_AddBtnActionPerformed

