    private void update() {
        try {
            DefaultTableModel m = (DefaultTableModel) EmployeeTable.getModel();
            String id = EmployeeIdField.getText();
            String name = EmployeeNameField.getText();
            String position = EmployeePositionField.getText();
            String salary = EmployeeSalaryField.getText();
            String contact = ContactField.getText();
            String address = AddressField.getText();
            String date = DateField.getText();
            Connection conn = DatabaseConnector.connect();

            String sql = " Update employeetable"
                    + " set Name=?, Position=?, Salary=?, Contact=?, Address=?,Date=? "
                    + " where Id=?";
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setInt(7, Integer.parseInt(id));
            pst.setString(1, name);
            pst.setString(2, position);
            pst.setInt(3, Integer.parseInt(salary));
            pst.setString(4,contact);
            pst.setString(5,address);
            pst.setString(6,date);
            int row = EmployeeTable.getSelectedRow();

            int rs = pst.executeUpdate();

            if (rs > 0) {
                JOptionPane.showMessageDialog(rootPane, "Updated successfully");
                showTableData();
                formReset();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Updated faild");
            }

        } catch (ClassNotFoundException | SQLException ex) {
            System.out.println(ex.getMessage());
        }

    }

