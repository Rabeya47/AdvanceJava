    private void loadTableData() {
        try {
            Connection conn = DatabaseConnector.connect();
            DefaultTableModel model = (DefaultTableModel) pp_table.getModel();
            String sql = "select* from purches";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
             removeAllRow();
            while (rs.next()) {
                String name = rs.getString("Product_Name");
                int qun = rs.getInt("Quantity");
                double price = rs.getDouble("Price");
                Object[] row = {name, qun, price};
                model.addRow(row);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(SellIssue.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void fieldClear() {
        pp_name.setText(null);
        pp_quantity.setText(null);
        pp_price.setText(null);
    }
    
     private void removeAllRow() {
        DefaultTableModel model = (DefaultTableModel) pp_table.getModel();
        int rows = model.getRowCount();
        for (int i = rows - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }

