    private void pp_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pp_saveActionPerformed

        String name = pp_name.getText();
        String qun = pp_quantity.getText();
        String price = pp_price.getText();

        try {
            Connection conn = DatabaseConnector.connect();
            String sql = "INSERT INTO purches(Product_Name,Quantity,Price)"
                    + "values(?,?,?);";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, name);
            pst.setString(2, qun);
            pst.setString(3, price);
            int rs = pst.executeUpdate();

            if (rs > 0) {
                JOptionPane.showMessageDialog(rootPane, "Saved successfully");
                loadTableData();
                fieldClear();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Saved unsuccessful");
            }

        } catch (ClassNotFoundException | SQLException ex) {
            System.out.println(ex.getMessage());
        }

    }//GEN-LAST:event_pp_saveActionPerformed

